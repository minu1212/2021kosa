
public class Calulator {
	int result;
	
	public void add(int num1, int num2) {
		result = num1 + num2;
		System.out.println(result);
	}
	public void sub(int num1, int num2) {
		result = num1 - num2;
		System.out.println(result);
	}
	public void mul(int num1, int num2) {
		result = num1 * num2;
		System.out.println(result);
	}
	public void div(int num1, int num2) {
		result = num1 / num2;
		System.out.println(result);
	}
	
	
}
